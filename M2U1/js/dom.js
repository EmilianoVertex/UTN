function cambiarTexto() {
    let parrafo = document.getElementById("cambioDeTexto");
    parrafo.textContent = "Has cambiado el texto!!!";
    
}

function cambiarColor() {
    let parrafo = document.getElementById("cambioDeColor");
    parrafo.style.backgroundColor = "red";
}

function cambiarColorTexto() {
    let parrafo = document.getElementById("colorDeTexto");
    parrafo.style.color = "green";
}